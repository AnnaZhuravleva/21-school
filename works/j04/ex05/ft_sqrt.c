int	ft_sqrt(int nb)
{
	int delta;
	int z;

	z = 1;
	delta = 0;
	while (z <= nb / 2)
	{
		delta = nb - z * z;
		if (delta == 0)
		{
			return (z);
		}
		z++;
	}
	return (0);
}

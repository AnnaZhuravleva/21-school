#include "ft.h"

int		get_dim_x(char *str)
{
	int x;

	x = 0;
	while (str[0] != '\0' && str[0] != '\n')
	{
		x++;
		str++;
	}
	return (x);
}

int		get_dim_y(char *str)
{
	int y;

	y = 0;
	while (str[0] != '\0')
	{
		if (str[0] == '\n')
			y++;
		str++;
	}
	return (y);
}
